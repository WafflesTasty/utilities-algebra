package waffles.utils.algebra.algorithms;

/**
 * The {@code RankReveal} interface defines an algorithm that computes the rank of a matrix.
 *
 * @author Waffles
 * @since Apr 7, 2019
 * @version 1.0
 */
public interface RankReveal
{
	/**
	 * Returns the rank of the {@code RankReveal}.
	 * 
	 * @return  a matrix rank
	 */
	public abstract int rank();
}